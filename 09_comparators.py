# < Menor que
print("Menor que")
print(5 < 6)
print(6 < 5)
print(5 < 5)

# >= Mayor o igual que
print("Mayor o igual que")
print(2 >= 1)
print(2 >= 3)
print(2 >= 2)

# <= Menor o igual que
print("Menor o igual que")
print(1 <= 2)
print(2 <= 1)
print(2 <= 2)

# == Igual
print("Igual")
print(6 == 6)
print(5 == 2)

# != Distinto
print("Distinto")
print(6 != 10)
print(6 != 6)

# Casos especiales
print("Casos especiales")
print("Apple" == 'Apple')
print("Apple" == 'apple')
print("1" == 1)

