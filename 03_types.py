# string
my_name = "Nicolas"
my_name = "Jake"
print(f'Mi nombre es {my_name}')
print(type(my_name))

# int
my_age = 45
print(f'Mi edad es {my_age}')
print(type(my_age))

# boolean
is_single = True
print(f'Soy soltero? {is_single}')
print(type(is_single))
is_single = False
print(f'Soy soltero? {is_single}')
print(type(is_single))

# inputs
# La entrada de input se interpreta como string
my_age = input('¿Cuál es tu edad?')
print(f'Mi edad es {my_age}')
print(type(my_age))
